FEATURES OF BLOCK
*A block consists of chunks of code which is similiar to methods in ruby.
*Block has a name(user-defined name) exactly like methods.
*Codes in the block is always enclosed within braces{}.
*A block is always invoked(called) from a function with the same name as that of the block.(suppose you are having a block named test then you will have to call the block from a function named test)
*We invoke(call) a block from inside a function by using the yield statement.
*Whenever you write yield statement this statement will invoke the block from inside the function.

